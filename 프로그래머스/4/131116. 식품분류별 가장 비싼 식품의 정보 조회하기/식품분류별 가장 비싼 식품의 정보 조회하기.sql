SELECT A.CATEGORY, B.MAX_PRICE, A.PRODUCT_NAME
FROM FOOD_PRODUCT A,     
    (SELECT CATEGORY, MAX(PRICE) MAX_PRICE
    FROM FOOD_PRODUCT
    WHERE CATEGORY IN ('과자', '국', '김치', '식용유')
    GROUP BY CATEGORY
    )
    B
WHERE
    A.PRICE=B.MAX_PRICE AND
    A.CATEGORY = B.CATEGORY
ORDER BY MAX_PRICE DESC

